    public Perfil(String nombre) {
        initComponents();
        Query consulta = em.createNamedQuery("Usuario.findByNombre");
        consulta.setParameter("nombre",nombre );
        List<Usuario> listaUsuario = consulta.getResultList();
        Usuario u = listaUsuario.get(0);
        jLabelNombre.setText("<html><b>Nombre:  </b>"+u.getNombre()+"</html");
        jLabelApellido.setText("<html><b>Apellidos:  </b>"+u.getApellidos()+"</html");
        jLabelContraseña.setText("<html><b>Contraseña:  </b>"+u.getPassword()+"</html");
        jLabelEmail.setText("<html><b>E-mail:  </b>"+u.getEmail()+"</html");
        DateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        String fecha = formato.format(u.getFechaNac());
        jLabelNacimiento.setText("<html><b>Fecha Nacimiento:  </b>"+fecha+"</html");
        jLabel1.setText("<html><h1>"+"PERFIL"+"<h1></html>");
    }
        
